USE cpsc_304;
SET FOREIGN_KEY_CHECKS = 0;
DROP TABLE IF EXISTS RecipeHasTag;
DROP TABLE IF EXISTS RecipeHasIngredient;
DROP TABLE IF EXISTS RecipeHasEquipment;
DROP TABLE IF EXISTS RecipeHasTechnique;
DROP TABLE IF EXISTS Course;
DROP TABLE IF EXISTS Picture;
DROP TABLE IF EXISTS Equipment;
DROP TABLE IF EXISTS Administrator;
DROP TABLE IF EXISTS Comments;
DROP TABLE IF EXISTS Collection;
DROP TABLE IF EXISTS Message;
DROP TABLE IF EXISTS Tag;
DROP TABLE IF EXISTS Ingredient;
DROP TABLE IF EXISTS Recipe;
DROP TABLE IF EXISTS Usr;
DROP TABLE IF EXISTS Technique;
SET FOREIGN_KEY_CHECKS = 1;

CREATE TABLE Equipment (
	name VARCHAR(255) PRIMARY KEY
);

CREATE TABLE Technique (
	name VARCHAR(255) PRIMARY KEY,
	difficulty INT
);

CREATE TABLE Usr (
       UUID INT PRIMARY KEY,
       birthday DATE,
       email VARCHAR(255) NOT NULL,
       username VARCHAR(255) NOT NULL,
       name VARCHAR(255) NOT NULL,
       password VARCHAR(255) NOT NULL,
       city VARCHAR(255) ,
       postalCode VARCHAR(255) ,
       UNIQUE (email),
       UNIQUE (username)
);

CREATE TABLE Collection (
	name VARCHAR(255),
	userUUID INT,
	PRIMARY KEY (name, userUUID),
	FOREIGN KEY (userUUID) REFERENCES Usr(UUID)
		ON DELETE CASCADE
);

CREATE TABLE Administrator (
	UUID INT PRIMARY KEY,
	adminLevel VARCHAR(255),
	password  VARCHAR(255),
	email VARCHAR(255),
	FOREIGN KEY (UUID) references Usr(UUID)
		ON DELETE CASCADE
);

CREATE TABLE Course (
	UUID INT PRIMARY KEY,
	text LONGTEXT,
	length INT,
	name VARCHAR(255) NOT NULL,
	requirementName VARCHAR(255),
	creatorUUID INT,
	UNIQUE (name),
	FOREIGN KEY (requirementName) REFERENCES Equipment(name)
		ON DELETE SET NULL,
	FOREIGN KEY (creatorUUID) REFERENCES Administrator(UUID)
		ON DELETE SET NULL
);

CREATE TABLE Tag(
	name VARCHAR(255) PRIMARY KEY
);

CREATE TABLE Recipe (
	UUID INT PRIMARY KEY,
	name VARCHAR(255) NOT NULL,
	text LONGTEXT NOT NULL,
	averageScore FLOAT,
	estimatedTime FLOAT,
	uploaderUUID INT,
	FOREIGN KEY (uploaderUUID) REFERENCES Usr(UUID)
		ON DELETE SET NULL
);
